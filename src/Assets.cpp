void Assets::AddChar(char c, VectorObject* vo)
{
    vo->Flip(0, 1);
    vo->Center();    
    vo->CenterY(-CHAR_MIDPOINT);
    vo->Scale(CHAR_SCALE);
    _chars.insert(std::pair<char, VectorObject*>(c, vo));
}

VectorObject* Assets::GetChar(char c)
{
    const auto& fc = _chars.find(c);
    if(fc == _chars.end())
    {
        return _chars.at(' ');
    }
    return fc->second;
}

